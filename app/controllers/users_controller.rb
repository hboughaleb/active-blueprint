class UsersController < ApplicationController
  def index         # GET /users
  end

  def show          # GET /users/:id
  end

  def new           # GET /users/new
  end

  def create        # POST /users
  end

  def edit          # GET /users/:id/edit
  end

  def update        # PATCH /users/:id
  end

  def destroy       # DELETE /users/:id
  end
end
