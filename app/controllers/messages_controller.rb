class MessagesController < ApplicationController

  def index         # GET /messages
  end

  def show          # GET /messages/:id
  end

  def new           # GET /messages/new
  end

  def create        # POST /messages
  end

  def edit          # GET /messages/:id/edit
  end

  def update        # PATCH /messages/:id
  end

  def destroy       # DELETE /messages/:id
  end
end
