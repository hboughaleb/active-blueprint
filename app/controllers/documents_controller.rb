class DocumentsController < ApplicationController

  def index         # GET /documents
  end

  def show          # GET /documents/:id
  end

  def new           # GET /documents/new
  end

  def create        # POST /documents
  end

  def edit          # GET /documents/:id/edit
  end

  def update        # PATCH /documents/:id
  end

  def destroy       # DELETE /documents/:id
  end
end
